#include "pushswap.h"

int	checknbr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i] < '0' || str[i] > '9' ) && (str[i] != '-' && str[i] != '+'))
		{
			write(2, "Error", 5);
			exit(2);
		}
		if ((str[i] == '-' || str[i] == '+')
			&& (str[i + 1] == '-' || str[i + 1] == '+'))
		{
			write(2, "Error", 5);
			exit(2);
		}
		i++;
	}
	return (1);
}